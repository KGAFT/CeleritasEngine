unsigned int *skyBoxIndices;

Mesh* skyboxMesh = nullptr;

Mesh* acquireSkyboxMesh(VulkanDevice* device){
        if(skyboxMesh==nullptr){
                skyBoxIndices = (unsigned int*)calloc(sizeof(skyboxVertices)/(3*sizeof(float)), sizeof(unsigned int));
                for(unsigned int i = 0; i<sizeof(skyboxVertices)/(3*sizeof(float)); i++){
                        skyBoxIndices[i] = i;
                }
                skyboxMesh = new Mesh(new VertexBuffer(sizeof(float)*3, (unsigned int) (sizeof(skyboxVertices)/(3*sizeof(float)), device, skyboxVertices), device, skyboxVertices), new IndexBuffer(device, skyBoxIndices, sizeof(skyboxVertices)/(3*sizeof(float))));

        }
        return skyboxMesh;
}